import { mkdir, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';

const sourceUrl = 'https://raw.githubusercontent.com/uberspot/OpenTriviaQA/master/categories/geography';
const output = resolve('src/generated/openTriviaGeography.ts');
const limit = Number(process.argv[2] ?? 30);

const response = await fetch(sourceUrl, { headers: { 'User-Agent': 'Kizz/0.1 educational importer' } });
if (!response.ok) throw new Error(`OpenTriviaQA: HTTP ${response.status}`);
const source = await response.text();

const questions = source.split(/\r?\n\r?\n+/).map((block) => {
  const lines = block.split(/\r?\n/).map((line) => line.trim()).filter(Boolean);
  const prompt = lines.find((line) => line.startsWith('#Q '))?.slice(3);
  const correct = lines.find((line) => line.startsWith('^ '))?.slice(2);
  const choices = ['A ', 'B ', 'C ', 'D '].map((prefix) => lines.find((line) => line.startsWith(prefix))?.slice(2));
  if (!prompt || !correct || choices.some((choice) => !choice)) return null;
  const answerIndex = choices.indexOf(correct);
  if (answerIndex < 0) return null;
  return { prompt, correct, choices, answerIndex };
}).filter(Boolean).slice(0, limit);

const normalized = questions.map((question, index) => {
  const capitalMatch = question.prompt.match(/^What is the capital of (.+)\?$/i);
  const prompt = capitalMatch ? `Quelle est la capitale de ${capitalMatch[1]} ?` : question.prompt;
  const difficulty = index < Math.ceil(limit * 0.35) ? 1 : index < Math.ceil(limit * 0.75) ? 2 : 3;
  return {
    id: `otqa-geo-${String(index + 1).padStart(3, '0')}`,
    topicId: 'geography', difficulty, prompt,
    choices: question.choices, answerIndex: question.answerIndex,
    explanation: `${question.correct} est la bonne réponse. Utilise le lien pour replacer cette capitale dans son contexte.`,
    tags: ['capitales', 'open-trivia-qa'],
    sourceLabel: 'OpenTriviaQA — CC BY-SA 4.0',
    sourceUrl: 'https://github.com/uberspot/OpenTriviaQA',
    learnMoreUrl: `https://fr.wikipedia.org/w/index.php?search=${encodeURIComponent(question.correct)}`,
  };
});

const moduleSource = `// Generated by scripts/import-opentriviaqa.mjs. Do not edit manually.\nimport { QuestionSeed } from '../domain';\n\nexport const openTriviaGeographyQuestions: QuestionSeed[] = ${JSON.stringify(normalized, null, 2)};\n`;
await mkdir(dirname(output), { recursive: true });
await writeFile(output, moduleSource, 'utf8');
console.log(`Generated ${normalized.length} questions in ${output}`);
